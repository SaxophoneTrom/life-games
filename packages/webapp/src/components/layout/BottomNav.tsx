'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useTranslation } from '@/components/i18n/LanguageContext';

const navItems = [
  { href: '/', key: 'home' as const, icon: HomeIcon },
  { href: '/buy', key: 'buy' as const, icon: BuyIcon },
  { href: '/gallery', key: 'gallery' as const, icon: GalleryIcon },
  { href: '/my', key: 'my_page' as const, icon: MyIcon },
];

export function BottomNav() {
  const pathname = usePathname();
  const t = useTranslation();

  return (
    <nav className="fixed bottom-0 left-0 right-0 h-20 bg-[#0B0F14] border-t border-white/10 z-50">
      <div className="flex items-center justify-around h-14 max-w-[424px] mx-auto">
        {navItems.map((item) => {
          const isActive =
            item.href === '/'
              ? pathname === '/'
              : pathname.startsWith(item.href);
          const Icon = item.icon;

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center gap-0.5 px-4 py-2 transition-colors ${
                isActive
                  ? 'text-[#F67280]'
                  : 'text-white/50 hover:text-white/80'
              }`}
            >
              <Icon className="w-5 h-5" />
              <span className="text-[10px] font-medium">{t(item.key)}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

